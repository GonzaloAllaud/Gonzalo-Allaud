def listar_jedis_por_rank(jedis, rank):
    "Listar los nombres de los Jedis que tienen un rank específico"
    return [jedi['name'] for jedi in jedis if jedi['rank'] and rank.lower() in jedi['rank'].lower()]

jedis_grand_master = listar_jedis_por_rank(jedis, 'Grand Master')

print("\nJedis con el rango de Grand Master:")
