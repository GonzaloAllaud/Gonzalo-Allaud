# A
def ordenar_jedis_por(jedis, criterio):
    "Ordenar los Jedis por un criterio dado ('name' o 'species')"
    return sorted(jedis, key=lambda jedi: (jedi[criterio] or '').lower())

jedis_ordenados_por_nombre = ordenar_jedis_por(jedis, 'name')
jedis_ordenados_por_especie = ordenar_jedis_por(jedis, 'species')

print("Jedis ordenados por nombre:")
for jedi in jedis_ordenados_por_nombre:
    print(jedi['name'])

print("\nJedis ordenados por especie:")
for jedi in jedis_ordenados_por_especie:
    print(f"{jedi['name']} ({jedi['species']})")

# B
def buscar_jedi_por_nombre(jedis, nombre):
    "Buscar un Jedi por nombre"
    return next((jedi for jedi in jedis if jedi['name'].lower() == nombre.lower()), None)

ahsoka_tano = buscar_jedi_por_nombre(jedis, 'Ahsoka Tano')
kit_fisto = buscar_jedi_por_nombre(jedis, 'Kit Fisto')

print("\nInformación de Ahsoka Tano:")
print(ahsoka_tano)

print("\nInformación de Kit Fisto:")
print(kit_fisto)

# C
def listar_padawans_de(jedis, maestro):
    "Listar todos los padawans de un maestro dado"
    return [jedi['name'] for jedi in jedis if jedi['master'] and maestro in jedi['master'].split('/')]

padawans_yoda = listar_padawans_de(jedis, 'Yoda')
padawans_luke = listar_padawans_de(jedis, 'Luke Skywalker')

print("\nPadawans de Yoda:")
print(padawans_yoda)

print("\nPadawans de Luke Skywalker:")
print(padawans_luke)

# D
def listar_jedis_por_especie(jedis, especies):
    "Listar los nombres de los Jedis de las especies especificadas"
    return [jedi['name'] for jedi in jedis if jedi['species'] and jedi['species'].lower() in [especie.lower() for especie in especies]]

especies = ['Human', 'Twi\'lek']
jedis_humanos_twilek = listar_jedis_por_especie(jedis, especies)

print("\nJedis de especie humana y twi'lek:")
print(jedis_humanos_twilek)

# E
def listar_jedis_por_letra_inicial(jedis, letra_inicial):
    "Listar los nombres de los Jedis que comienzan con una letra específica"
    return [jedi['name'] for jedi in jedis if jedi['name'].startswith(letra_inicial)]

jedis_comienzan_con_a = listar_jedis_por_letra_inicial(jedis, 'A')

print("\nJedis que comienzan con 'A':")
print(jedis_comienzan_con_a)

# F
def listar_jedis_con_multiples_colores(jedis):
    "Listar los nombres de los Jedis que usaron sable de luz de más de un color"
    return [jedi['name'] for jedi in jedis if jedi['lightsaber_color'] and '/' in jedi['lightsaber_color']]

jedis_multiples_colores = listar_jedis_con_multiples_colores(jedis)

print("\nJedis que usaron sable de luz de más de un color:")
print(jedis_multiples_colores)

# G
def listar_jedis_por_color_especifico(jedis, colores):
    "Listar los nombres de los Jedis que usaron sable de luz de colores específicos"
    return [jedi['name'] for jedi in jedis if jedi['lightsaber_color'] and any(color.lower() in jedi['lightsaber_color'].lower() for color in colores)]

colores_especificos = ['Yellow', 'Purple']
jedis_colores_especificos = listar_jedis_por_color_especifico(jedis, colores_especificos)

print("\nJedis que usaron sable de luz amarillo o violeta:")
print(jedis_colores_especificos)

